import com.example.demo.entity.RSAKey;

@Controller
public class controller_generateKey {

	
	private RSAKey key;

	@Autowired
	public controller_generateKey(RSAKey key) {
		this.key = key;
	}


	@GetMapping("/generate-key") 
	public String taokey() {
		return "taokey";
	}

	private void addToZipFile(List<String> srcFiles, ZipOutputStream zipOut) throws IOException {
		for (String srcFile : srcFiles) {
            File fileToZip = new File(srcFile);
            FileInputStream fis = new FileInputStream(fileToZip);
            ZipEntry zipEntry = new ZipEntry(fileToZip.getName());
            zipOut.putNextEntry(zipEntry);

            byte[] bytes = new byte[1024];
            int length;
            while((length = fis.read(bytes)) >= 0) {
                zipOut.write(bytes, 0, length);
            }
            fis.close();
        }
	}
}
